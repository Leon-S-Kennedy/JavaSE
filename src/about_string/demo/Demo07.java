package about_string.demo;

public class Demo07 {
    public static void main(String[] args) {
        String s;
        s=String.valueOf((byte)1);
        s=String.valueOf((short)1);
        s=String.valueOf(1);
        s=String.valueOf(1L);
        s=String.valueOf(1.0F);
        s=String.valueOf(1.0);
        s=String.valueOf('c');
        s=String.valueOf(true);
    }
}
